package com.michael.annotation;

/**
 * 内置变量
 *
 * @author Michael
 */
public enum InnerVariable {
    /**
     * 实体
     */
    ENTITY_NAME,
    /**
     * 项目根路径（要生成的文件的根）
     */
    ROOT_PATH,
    /**
     * 模块名称
     */
    MODULE_NAME,
    /**
     * 作者
     */
    AUTHOR,
    /**
     * 包名称
     */
    PACKAGE_NAME
}
